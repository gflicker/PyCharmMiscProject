#Rearranging elements in a list using sort function
my_Siblings = ["Sunwell", "Haggai", "Kelvin", "Precious", "Persida", "Tristan"]
print("Here is the original list:")
print(my_Siblings)

#Sorting it permanently
my_Siblings.sort()
print(my_Siblings)

#Sorted alphabetically in reverse order
my_Siblings.sort(reverse=True)
print(my_Siblings)